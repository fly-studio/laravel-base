    {
       $this->validate($request, [
            'username' => 'required|unique:member|max:150',
            'password' => 'required|min:6',
        ]);
        Member::create([
            'username' => $request->input('username'),
            'password' => bcrypt($request->input('password')),
            'rid' => 1,
        ]);
    }

